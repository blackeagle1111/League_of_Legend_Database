-- Teams compete in 2011 events that have 5 team members?

SELECT T.tName,COUNT(playerID) FROM playerChampionTeamMatch PC 
	INNER JOIN teams T ON T.teamID=PC.teamID
	INNER JOIN matches M ON M.mID=PC.matchID
	INNER JOIN matchHeldAtEvent ME ON ME.matchID=M.mID
	INNER JOIN gameEvents GE ON GE.gEventID=ME.eventID
 	WHERE GE.gStartDate>='2011-01-01' AND GE.gEndDate<='2011-12-31'
	GROUP BY T.tName
		HAVING COUNT(playerID)=5;


