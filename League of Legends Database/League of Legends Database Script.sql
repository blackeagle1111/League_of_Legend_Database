CREATE TABLE champions
(
    cid   	 		INTEGER NOT NULL AUTO_INCREMENT,
    cName   	 	VARCHAR(20) NOT NULL,
    cReleaseDate    DATE NOT NULL,
    cIpCost   	 	INTEGER NOT NULL,
    cRpCost   	 	INTEGER NOT NULL,
    CONSTRAINT champions_PK PRIMARY KEY (cid),
	CONSTRAINT champions_CK UNIQUE (cName)
);

CREATE TABLE abilities
(
   championID   	INTEGER NOT NULL,
   aName       		VARCHAR(40) NOT NULL,
   aManaCost      	INTEGER,
   aRange       	INTEGER,
   aDescription   	VARCHAR(350),  
   CONSTRAINT abilities_pk PRIMARY KEY (aName),
   CONSTRAINT abilities_champions_FK FOREIGN KEY (championID) REFERENCES champions(cid)
);

CREATE TABLE companies
(
    cpName			VARCHAR(20) NOT NULL,
    cpWebsite		VARCHAR(40) NOT NULL,
    CONSTRAINT companies_pk PRIMARY KEY (cpName),
	CONSTRAINT companies_ck UNIQUE (cpWebsite)
);

CREATE TABLE gameEvents
(
    gEventID		CHAR(5) NOT NULL,
    gName			VARCHAR(100) NOT NULL,
    gStartDate		DATE NOT NULL,
    gEndDate		DATE NOT NULL,
    gCountry		VARCHAR(15) NOT NULL,
    CONSTRAINT events_pk PRIMARY KEY (gEventID),
	CONSTRAINT events_ck UNIQUE (gName, gStartDate)
);

CREATE TABLE items
(
    iName			VARCHAR(25) NOT NULL,
    iCost 			FLOAT NOT NULL,
	iEffect			VARCHAR(255) NOT NULL,
    CONSTRAINT items_pk PRIMARY KEY (iName),
	CONSTRAINT items_ck UNIQUE (iEffect)
);

CREATE TABLE regions
(
    rAbbrev			CHAR(5) NOT NULL,
    rName			VARCHAR(20) NOT NULL,
    CONSTRAINT regions_PK PRIMARY KEY (rAbbrev),
	CONSTRAINT regions_CK UNIQUE (rName)
);

CREATE TABLE matches
(	
    mID				INTEGER NOT NULL AUTO_INCREMENT,	
    mDatePlayed		DATE NOT NULL,
   CONSTRAINT matches_pk PRIMARY KEY (mID)
);

CREATE TABLE maps
(
    mpName				VARCHAR(25) NOT NULL,
    mpPlayerCapacity	INTEGER NOT NULL,
    CONSTRAINT maps_PK PRIMARY KEY (mpName)
);

CREATE TABLE teams
(
    teamID			INTEGER NOT NULL AUTO_INCREMENT,
    tName			VARCHAR(25) NOT NULL,
    tNumWin			INTEGER NOT NULL,
    tNumLoss		INTEGER NOT NULL,
    CONSTRAINT team_pk PRIMARY KEY (teamID)
);

CREATE TABLE players
(
    pid				INTEGER NOT NULL AUTO_INCREMENT,
    pDob			DATE NOT NULL,
    pUsername 		VARCHAR(10) NOT NULL,
    pEmail 			VARCHAR(30) NOT NULL,
    pCreationDate	DATE NOT NULL,
    regionAbbrev 	CHAR(5),
    CONSTRAINT player_pk PRIMARY KEY (pid),
    CONSTRAINT player_region_FK FOREIGN KEY (regionAbbrev) REFERENCES regions (rAbbrev),
	CONSTRAINT player_ck1 UNIQUE (pUsername),
	CONSTRAINT player_ck2 UNIQUE (pEmail)
);

CREATE TABLE mapPlayedOn
(
    matchID			INTEGER NOT NULL,
    mapName			VARCHAR(25) NOT NULL,
    CONSTRAINT mapPlayedOn_PK PRIMARY KEY (matchID, mapName),
    CONSTRAINT mapPlayedOn_match_FK FOREIGN KEY (matchID) REFERENCES matches (mID),
    CONSTRAINT mapPlayedOn_map_Fk FOREIGN KEY (mapName) REFERENCES maps (mpName)
);

CREATE TABLE playerChampionTeamMatch
(
    playerID   		INTEGER NOT NULL,
	teamID   		INTEGER,
    matchID   		INTEGER NOT NULL,
    championID   	INTEGER NOT NULL,
    wonGame   		TINYINT(1) NOT NULL,
    CONSTRAINT playerChampionTeamMatch_PK PRIMARY KEY (playerID, matchID, championID),
    CONSTRAINT playerChampionTeamMatch_champions_FK FOREIGN KEY (championID)
        REFERENCES champions (cid) ON DELETE CASCADE,
    CONSTRAINT playerChampionTeamMatch_teams_FK FOREIGN KEY (teamID)
        REFERENCES teams (teamID) ON DELETE CASCADE,
    CONSTRAINT playerChampionTeammatch_matches_FK FOREIGN KEY (matchID)
        REFERENCES matches (mID) ON DELETE CASCADE,
    CONSTRAINT playerChampionTeamMatch_player_FK FOREIGN KEY (playerID)
        REFERENCES players (pid)  ON DELETE CASCADE   
);

CREATE TABLE companySponsorsTeam
(
	companyName		VARCHAR(20) NOT NULL,
	teamID    		INTEGER NOT NULL,
	CONSTRAINT companySponsorsTeam_PK PRIMARY KEY (companyName,teamID),
	CONSTRAINT companySponsorsTeam_companies_FK FOREIGN KEY (companyName)
        REFERENCES companies (cpname),
    CONSTRAINT companySponsorsTeam_teams_FK FOREIGN KEY (teamID) 
        REFERENCES teams (teamID)
);

CREATE TABLE matchHeldAtEvent
(
    matchID			INTEGER NOT NULL,
    eventID			CHAR(5) NOT NULL,
    CONSTRAINT matchHeldAtEvent_PK PRIMARY KEY (matchID, eventID),
    CONSTRAINT matchHeldAtEvent_m_FK FOREIGN KEY (matchID) 
        REFERENCES matches (mID),
    CONSTRAINT matchHeldAtEvent_e_FK FOREIGN KEY (eventID) 
        REFERENCES gameEvents (gEventID)
);

CREATE TABLE recommendedItems
(
    championID		INTEGER NOT NULL,
    itemName		VARCHAR(25) NOT NULL,
    CONSTRAINT recommendedItems_PK PRIMARY KEY(championID, itemName),
    CONSTRAINT recommendedItems_champions_FK FOREIGN KEY (championID)
		REFERENCES champions (cid),
    CONSTRAINT recommendedItems_items_FK FOREIGN KEY (itemName)
		REFERENCES items (iname)
);

CREATE TABLE playStats
(
    pid				INTEGER NOT NULL,
    statType		VARCHAR(25) NOT NULL,
    statValue		INTEGER NOT NULL,
    CONSTRAINT playStats_pid_PK PRIMARY KEY (pid, statType),
    CONSTRAINT playStats_pid_FK FOREIGN KEY (pid) REFERENCES players (pid)
);

CREATE TABLE characterStats
(
   cid				INTEGER NOT NULL,
   statType			VARCHAR(25) NOT NULL,
   statValue		INTEGER NOT NULL,
   CONSTRAINT champStats_PK PRIMARY KEY (cid, statType),
   CONSTRAINT champStats_FK FOREIGN KEY (cid) REFERENCES champions (cid)
);

