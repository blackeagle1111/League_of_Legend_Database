-- Query to find the recommended item with the lowest cost for each champioin
SELECT cid, cName, iname, iCost
	FROM champions c
	INNER JOIN recommendedItems r ON c.cid=r.championID
	INNER JOIN items i ON r.itemName=i.iname
GROUP BY cid
HAVING NOT iCost > ANY(SELECT iCost
					FROM recommendedItems r
					INNER JOIN items i ON r.itemName=i.iname
					WHERE c.cid=r.championID);

