-- which team is the top ranking team in all 2011 season events?

SELECT T.tName AS TEAM_NAME,MAX(tNumWin/tNumLoss) AS RATIO 
FROM teams T INNER JOIN playerChampionTeamMatch P ON T.teamID=P.teamID 
			 INNER JOIN matches M ON M.mID=P.matchID
			 INNER JOIN matchHeldAtEvent ME ON ME.matchID=M.mID
			 INNER JOIN gameEvents GE ON GE.gEventID=ME.eventID
WHERE GE.gStartDate>='2011-01-01' AND GE.gEndDate<='2011-12-31'
GROUP BY T.tName
HAVING MAX(tNumWin/tNumLoss)>=ALL (SELECT MAX(tNumWin/tNumLoss) 
FROM teams T1 INNER JOIN playerChampionTeamMatch P1 ON T1.teamID=P1.teamID 
			  INNER JOIN matches M1 ON M1.mID=P1.matchID
			  INNER JOIN matchHeldAtEvent ME1 ON ME1.matchID=M1.mID
			  INNER JOIN gameEvents GE1 ON GE1.gEventID=ME1.eventID
WHERE GE1.gStartDate>='2011-01-01' AND GE1.gEndDate<='2011-12-31'
GROUP BY T1.tName);
					
